
public class ExemploTipoPrimitivo2 {
	public static void main(String[] args) {
		//definindo explicitamentte que o valor é float
		float a=10.99f;
		//definindo double
		double b = 10.3D;
		//Atribuindo valor inteiro para um double
		double c = 5;
		// atribuindo um valor, por padrão todos os numeros com casas decimais sao do tipo double
		double d = 7.2;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}

}
